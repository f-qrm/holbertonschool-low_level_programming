#include "3-calc.h"
/**
 * op_add - Effectue l'addition de a et b
 *@a: Le premier entier
 *@b: Le deuxième entier
 *Return: Le résultat de l'addition
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Effectue la soustraction de a et b
 *@a: Le premier entier
 *@b: Le deuxième entier
 *Return: Le résultat de la soustraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Effectue la multiplication de a et b
 *@a: Le premier entier
 *@b: Le deuxième entier
 *Return: Le résultat de la multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Effectue la division de a par b
 *@a: Le premier entier
 *@b: Le deuxième entier
 *Return: Le résultat de la division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Effectue le modulo de a par b
 *@a: Le premier entier
 *@b: Le deuxième entier
 *Return: Le résultat du modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}
